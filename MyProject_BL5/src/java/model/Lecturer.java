package model;

/**
 *
 * @author HP
 */
public class Lecturer {
    private int lid ;
    private String lname ;

    public Lecturer(int lid, String lname) {
        this.lid = lid;
        this.lname = lname;
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public Lecturer() {
    }
    
}
